# This is the main folder for the simulation of paper trading
# models.py contains the models of userdetails(such as cash, profit, portfolio and their trade history along with the stocks and shares they own)
# views.py contains the functions to view user portfolio, their history, live stock data , and trade the stocks from the live stock data
# urls.py contains the urls to view dashboard that is the portfolio, history, and trade and view
# A user can also view other users trade history to learn from them
# A leaderboard page contains a list of user sorted by their profits and giving each of them a rank.
# A timeseries chart of few companies can also be seen using the chart.js library.
